public class Animal {
    void preethi() {
        System.out.println("HELLO");
    }
}

class Animal2 extends Animal {
    void Shreyas() {
        System.out.println("kudikala");
    }
}

class inheritance2 {
    public static void main(String[] args) {
        Animal2 pr = new Animal2();
        pr.preethi();
        pr.Shreyas();

    }
}
